                    <p class="text">リビングと玄関ホールには、折り上げ天井を採用。一段高くなったことで縦に空間が広がり、さらに居住スペースをワイドに感じさせる効果も生まれます。また、玄関ホール・廊下・全室には遮音性に優れた高級感のある天然木フローリングを施しています。あたたかな木の温もり、しっとりとした木の感触、静かな木の息づかい、柔らかな質感が洗練された雰囲気を演出し、気品溢れる住空間を実現します。</p>
