        <h4 class="title-h3 ta_c">2種類から選べる暮らしに嬉しい<br>｢調理器具セレクトシステム｣</h4>
        <p class="text tab-section__text">システムキッチンに採用した｢調理器具セレクトシステム｣とはキッチンの新しい提案の一つ。各住戸の電気量200ボルト配線を確保した上で、キッチンにガスと電気の二系統の熱源をあらかじめ配しました。これにより、キッチンコンロはホーロートップガスコンロとIHクッキングヒーターのいずれかをお選びいただけ、将来のキッチンリフォーム時に特別な屋内工事をすることなく、調理器具変更にも容易に対応できます。今回は2つの魅力的な調理器具を無料セレクト可能とし、得意な料理に合わせてお選びいただけます。タカラスタンダードのシステムキッチンには、毎日のキッチンワークを徹底的にサポートする｢快適｣機能が充実。他に例のないホーロートッププレートのガスコンロは、IHクッキングプレートと同様に掃除がしやすく、さらに手軽に取り出せる単独ゴトク採用でいつもきれいな状態をキープできます。また、グリル部は魚を裏返さずに両面焼き上げ、左右コンロは便利な揚げ物温度調節機能付で油の温度上昇による発火も防ぎます。そして、もうひとつが鍋自体を発熱させるから高火力ながらも、周囲に熱が逃げにくくキッチン内が暑くなりにくいIHクッキングヒーター。魚を水なしで裏返さずに焼けるグリル付き。フラットなトッププレートなのでお手入れも簡単で日々の料理をサポートします。</p>
